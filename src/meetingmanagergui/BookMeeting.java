    private void btnSendRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendRequestActionPerformed
       
       var meetingPlats =lblPlats.getText();
       var meetingTid = lblTid.getText();
       var senderID = employee.getID();
       
       if(Employees.size() > 0 )
       {
        for(int i = 0; i < Employees.size(); i++)
        {
                var meetingID= _meetingService.getMaxIdMeeting();
               _meetingService.addMeeting(meetingID, meetingPlats, meetingTid, meetingDatum, Employees.get(i).getID(), senderID, "U");
        }
       }
       else
       {
        JOptionPane.showMessageDialog(null, "Du har inte valt någon till mötet!");
        return;
       }
      JOptionPane.showMessageDialog(null, "Din mötesförfrågan är skickad");
      this.dispose();
      
       
       
    }//GEN-LAST:event_btnSendRequestActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        
     var userNameAndFirstName = listAllEmployees.getSelectedValue();
     
     if(userNameAndFirstName == null)
     {
         JOptionPane.showMessageDialog(null, "Du måste välja någon från listan");
     }
   
     
    if(userNameAndFirstName.contains(" ")){
      userNameAndFirstName= userNameAndFirstName.substring(0, userNameAndFirstName.indexOf(" ")); 
    }  
    var userName = userNameAndFirstName;
    
    var employee = _employeService.getEmployeebyUsername(userName);
    
    DefaultListModel employeelist = new DefaultListModel();
    list_employeesForMeeting.setModel(employeelist);
               
      
      Employees.add(employee);
               
      for(int i = 0 ; i< Employees.size(); i++)
      {
            employeelist.add(i, Employees.get(i).getUsernameAndFirstName());
            
      }
     
      
    }//GEN-LAST:event_btnAddActionPerformed

